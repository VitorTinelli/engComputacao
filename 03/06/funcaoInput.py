# Funcao Input

# A funcao input() permite que receber dados do usuario. (Scanner em Java)
# input() sempre retorna uma string.

# Exemplo 1
nome = input("Digite seu nome: ")
print("Olá", nome)

# Para alterar o tipo de dado, usar as funcoes int(), float() ou str()
idade = int(input("Digite sua idade: "))
print("Sua idade é:", idade)