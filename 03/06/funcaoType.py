# Funcao type() retorna o tipo de dado

nome = input("Digite seu nome: ")
idade = int(input("Digite sua idade: "))
altura = float(input("Digite sua altura: "))
boolean = bool(input("Digite True ou False: "))

print ("Nome: ", nome, " - Tipo: ", type(nome))
print ("Idade: ", idade, " - Tipo: ", type(idade))
print ("Altura: ", altura, " - Tipo: ", type(altura))
print ("Boolean: ", boolean, " - Tipo: ", type(boolean))
